let farenheit = 32 // 0c -> 273.15K

// calculate celsius and store in celsius variable
// print that value
let celsius = (farenheit - 32) * 5 / 9
console.log(celsius) // eslint-disable-line no-console

// calculate kelvin value and store in variable
// print that value
let kelvin = (farenheit + 459.67) * 5 / 9
console.log(kelvin) // eslint-disable-line no-console
